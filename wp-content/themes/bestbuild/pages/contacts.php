  jQuery(document).ready(function() {
    // Variables
    var clickedTab = jQuery(".tabs > .active");
    var tabWrapper = jQuery(".tab__content");
    var activeTab = tabWrapper.find(".active");
    var activeTabHeight = activeTab.outerHeight();
    // Show tab on page load
    activeTab.show();
    // Set height of wrapper on page load
    tabWrapper.height(activeTabHeight);
    jQuery(".tabs > li").on("click", function() {
      // Remove class from active tab
      jQuery(".tabs > li").removeClass("active");
      // Add class active to clicked tab
      jQuery(this).addClass("active");
      // Update clickedTab variable
      clickedTab = jQuery(".tabs .active");
      // fade out active tab
      activeTab.fadeOut(250, function() {
        // Remove active class all tabs
        jQuery(".tab__content > li").removeClass("active");
        // Get index of clicked tab
        var clickedTabIndex = clickedTab.index();
        // Add class active to corresponding tab
        jQuery(".tab__content > li").eq(clickedTabIndex).addClass("active");
        // update new active tab
        activeTab = jQuery(".tab__content > .active");
        // Update variable
        activeTabHeight = activeTab.outerHeight();
        // Animate height of wrapper to new tab height
        tabWrapper.stop().delay(50).animate({
          height: activeTabHeight
        }, 500, function() {
          // Fade in active tab
          activeTab.delay(50).fadeIn(250);
        });
      });
    });
    // Variables
    var colorButton = jQuery(".colors li");
    colorButton.on("click", function() {
      // Remove class from currently active button
      jQuery(".colors > li").removeClass("active-color");
      // Add class active to clicked button
      jQuery(this).addClass("active-color");
      // Get background color of clicked
      var newColor = jQuery(this).attr("data-color");
      // Change background of everything with class .bg-color
      jQuery(".bg-color").css("background-color", newColor);
      // Change color of everything with class .text-color
      jQuery(".text-color").css("color", newColor);
    });
  });
</script>
