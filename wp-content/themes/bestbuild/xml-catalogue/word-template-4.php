          <h3>Предварительный зерноочиститель с камнеотделителем</h3>
          <p> Предварительный зерноочиститель с камнеотделителем от Akyurek Technology хорошо зарекомендовал себя благодаря эффективной концепции.
Отделение сора на первом этапе процесса очистки позволяет повысить общую эффективность технологического оборудования, расположенного далее.
Данный зерноочиститель позволяет очищать большие объемы продукии без потери качества, особенно это ценится при поступлении продукта в начале сезона, когда очень важно время разгрузки оборудования.<br>
Работа машины «Предварительный зерноочиститель с камнеотделителем» делится на 3 этапа:<br>
Этап 1- Решетная лодка с 2 уровнями сит для скальпирования и просеивания, предназначенными для выделения крупного сора (такого как части стеблей и большие камни) и легких примесей (таких как песок и мелкая пыль), разделенные специальным решетом.<br>
Стадия 2-Камнеотделение с интенсивным разделением по плотности, удаляются более тяжелые примеси (камни, стекло) <br>
Этап 3- Решетная лодка с единым уровнем просеивания для отделения колотых ядер продукции, проводится на очищенном от камней сырье. <br>Принцип работы: <br>Продукт поступает в очиститель через устройство подачи с переменной скоростью для контроля расхода потока.   Роликовый податчик равномерно распределяет продукт и переносит его на решетный стан первого уровня, где отделяется крупная продукция, и все, что меньше крупной фракции поступает на второй решетный блок. Грязь и песок удаляются перфорированными просеивающими ситами. Как только грязь и песок удаляются, продукт транспортируется к камнеотделителю. Плотный поток воздуха, генерируемый нижним вентилятором для стратификации, направляет легкий сор в направлении, противоположном ходу продукции. Легкие частицы пыли проходят через вентиляционную систему, а затем в пылесборный блок. Оставшийся в проходе продукт продвигается вниз по решету и проходит через другую серию перфорированных сит для удаления любых, возможных на этом этапе, видов сора.
