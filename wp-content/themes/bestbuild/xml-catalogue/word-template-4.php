    Аспирация: В аспирационной секции отделяются листья, части стеблей и другой легкий сор, меньший, чем очищаемая продукция. 
          <br>
          Решета: Равномерное распределение продукции по площади сита является важнейшим аспектом работы любого решетного сепаратора. Эта модель предоставляется в 4-х вариантах габаритов, для предоставления клиентам максимально допустимой площади для очистки. 
 <br>
          Просеивание: Комбинации сменных панелей и решет необходимы для достижения желаемых результатов в скальпировании или просеивании. Оборудование спроектировано так, чтобы замена рабочих частей происходила максимально оперативно. 
          <br>
          Система очистки решет: Для поддержания высокой производительности и гигиеничности процесса, в нашем оборудовании не используются тканные системы очистки сит. Вместо этого применяются шариковые лотки, которые в течение рабочего процесса, соприкасаются с рабочей областью решет. 
          <br>
     Многопоточная система: Каждая машина Akyurek снабжена возможностью подключения дополнительного оборудования, например  аспирации. Размеры, положение и направленность потока могут быть разными, в зависимости от потребностей заказчика.      
<br>
        Камнеотделитель: Предварительный очиститель разработан для смещения уровней продукции на рабочем ходу, позволяя тяжелому сору и камням подниматься в верхние слои для дальнейшего вывода в отсев.
        

        
