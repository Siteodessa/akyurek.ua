          <h3>Вентилятор центробежный ACF</h3>
          <p>

The Akyurek centrifugal fan type ACF is a medium pressure fan having backward curved steel blades manufactured in steel plate. The impeller is self-cleaning and dynamically balanced. The spiral housing is made of welded steel plate.

The fans are supplied with a direct coupled 1450 rpm flange motor. The larger models, ACF620, 625 and 630 are supplied with motor frame and built-in Varifan on the suction side.

The ACF fans are as standard equipped with trottle valves and can be supplied with the specially developed VARIFAN (see drawing) which is made of adjustable guide plates placed as a rosette at the intake. It regulates the air volume and reduces the energy consumption with lowered capacity.

Radial fans are designed to transportations with relatively high air resistance as well as a trasportation of the plastic bottles on the production line. Motors are able t run in temp between 15 and +40 Celsius's and humidity up to 95%



Центробежный вентилятор Akyurek типа ACF представляет собой вентилятор среднего давления, имеющий задние изогнутые стальные лопасти, изготовленные из стального листа. Рабочее колесо самоочищается и динамически сбалансировано. Спиральный корпус выполнен из сварной стальной плиты.

Вентиляторы снабжены фланцевым двигателем с прямым присоединением 1450 об / мин. Большие модели, ACF620, 625 и 630 поставляются с рамой двигателя и встроенным вентилятором со стороны всасывания.

Вентиляторы ACF в стандартной комплектации оснащены клапанами для тросов и могут поставляться со специально разработанным вентилятором (см. Чертеж), который изготовлен из регулируемых направляющих пластин, помещенных в розетку на входе. Он регулирует объем воздуха и снижает потребление энергии при пониженной производительности.



