          <h3>Вибрационный Сепаратор C.SAS -013 </h3>
          <div> Современный дизайн вибрационного решетного сепаратора позволяет ему достигать высококачественной очистки при больших объемах, несмотря на свою компактность и низкое энергопотребление. Предназначен для разделения зерновой массы на крупную и мелкую фракцию.  
<h5>Описание процесса   </h5>
Продукция загружается в машину посредством конвейера и продвигается к передней решетной корзине  где она равномерно рассеивается по поверхности сита. Возвратно-поступательные такты решет, создаваемые вибромоторами, направляя сырье в вертикальное положение, для прохождения через перфорированные решета.    
Akyurek C.SAS-013 был спроектирован таким образом, чтобы сырье двигалось по ситу медленнее к выпускному / высокому патрубку, чем к впускному / головному краю. Это приводит к увеличению запасов, сохраняющихся на сите, улучшению распределения сырья и увеличению емкости сита.
На первом сите примеси, превышающие размер зерновых, отделяются и удаляются из машины. Проход от первой решетной камеры поступает на вторую, где отделяются примеси, меньшие чем зерновая культура.</div>
